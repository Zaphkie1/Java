package repeticion;
/*
EJ3:imprime los primeros 20 numeros impares
usando la sentencia while
*/
public class NumerosImpares {
    public static void main(String[] args) {
        //Delcracion de variables
        int numero=1;//Inicializacion
        while(numero <=40)//condicion
        {
            System.out.println(numero+"\n");
            numero+=2;//numero = numero +2 incremento
        }
    }//Fin del metodo main()
}//Fin de la clase
