package basico;
/*
 * Comentario de mas de una linea mediante /*....
 * Ej1: Imprime saludos en diferentes idiomas
 */
public class Saludo {
	public static void main (String[] args) {
		
		System.out.println("               **** Saludos ****");
		System.out.println("------------------------------------------------");
		System.out.println("Mx ---> Hola Mundo !!!!");
		System.out.println("En ---> Hello World !!!!");
		System.out.println("It ---> Saluti Mundi !!!!");
		System.out.println("Ge ---> Hallo Welt !!!!");
		System.out.println("Po ---> Witaj swiecie !!!!");
	}//fin del metodo main ()
}//fin de la clase 