package basico;
/*
 * Ej7: Muestra el uso de variables y operadores
 * aritmeticos +, -, /, %
 */
public class TipoDeDatos {
	public static void main(String[] args) {
		// Declaracion de variables
		String nombre = null;
		int edad = 0;
		double peso = 0;
		char sexo = 0;
		boolean permiso = false;
		System.out.println(nombre);
		System.out.println(edad);
		System.out.println(peso);
		System.out.println(sexo);
		System.out.println(permiso);
		System.out.println("********************************");
		//asigna valores a las variables
		nombre="Javier perez";
		edad=22;
		peso=67.45;
		sexo='M';
		permiso=true;
		System.out.println(nombre);
		System.out.println(edad);
		System.out.println(peso);
		System.out.println(sexo);
		System.out.println(permiso);
		System.out.println("********************************");
		nombre="Estefania Salas";
		edad=21;
		peso=47.33;
		sexo='F';
		permiso=false;
		System.out.println(nombre);
		System.out.println(edad);
		System.out.println(peso);
		System.out.println(sexo);
		System.out.println(permiso);
		System.out.println("********************************");
	}//fin del metodo main()
}//fin de la clase
