package EjercicioRepeticion;
/*
Ej 4: Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando un bucle
for
*/
public class Ejercicio4 {
    public static void main(String[] args) {
        //Declaracion de variables
        System.out.println("-------------------------------------");
        System.out.println("\tNumeros For");
        System.out.println("-------------------------------------");
        for(int i = 320; i>=160;i-=20)
        System.out.println(i);
}//Fin del metodo main ()
}//Fin de la clase
