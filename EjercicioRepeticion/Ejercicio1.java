package EjercicioRepeticion;
/*
Ej 1: Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle for
*/
public class Ejercicio1 {
    public static void main(String[] args) {
        //Declaracion de variables
        System.out.println("-------------------------------------");
        System.out.println("\tNumeros For");
        System.out.println("-------------------------------------");
        for(int i = 0; i<=100;i+=5)
            System.out.println(i);
    }//Fin del metodo main ()
}//Fin de la clase